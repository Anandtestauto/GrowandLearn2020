package week1.d2;

public class Calculator {
	
	   
	
	
	public int add(int a,int b,int c) {
		
		return a+b+c;
	}
	
    public int sub(int x,int y) {
		
		return x-y;
	}

    public double mul(double x,double y) {
		
		return x*y;
	}

    
    public float div(float d,float e) {
		
		return d/e;
	}
  
}
